@implementation Math
- (float)addfunctWitha:(float)a andb:(float)b{
    return a+b;
}
- (float)minusfunctWitha:(float)a andb:(float)b{
    return a-b;
}
- (float)multiplyfunctWitha:(float)a andb:(float)b{
    return a*b;
}
- (float)dividefunctWitha:(float)a andb:(float)b{
    return a/b;
}
@end
